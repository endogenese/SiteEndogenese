                <h2>Entenda nosso...</h2>
                <h1><small> Nome</small></h1>
                <div style="text-align:justify; font-size:15px">
                  <p>Endogênese - A palavra é de origem grega, onde ENDO significa "posição interior, movimento para dentro", e GêNESE significa "origem, nascimento, criação, geração".</p><p>Conforme o dicionário on-line <a href="http://www.dicio.com.br/endogenese/" target="_blank" title="Dicio">Dicio</a>, Endogênese significa: "Característica de endógeno; qualidade do que se forma no interior de; processo de crescimento de uma célula no interior de outra; endogenia.". Endógeno, conforme o dicionário on-line <a href="http://michaelis.uol.com.br/moderno/portugues/index.php?lingua=portugues-portugues&palavra=end%F3geno" target="_blank" title="Michaelis">Michaelis</a>, por sua vez significa: "Que cresce de dentro de; Que se desenvolve dentro da parede de célula; Originado dentro do organismo.". Dessa forma surgiu a inspiração para o nome da empresa: Endogênese é a capacidade que certos organismos tem de se desenvolver a partir do seu interior; é o crescimento que vem de dentro. </p>
                  <p>Para alguns autores, é possível observar a endogênese (ou endogenia) na sociedade, quando, por exemplo, pessoas de uma determinada comunidade começam a se mobilizar para melhorar sua situação sócio-econômica, criando associações e cooperativas, promovendo festivais, criando postos de trabalho relacionadas ao turismo, artesanato, agroindústria, etc., e aos poucos se tornando agentes transforadores de sua própria realidade. Ou seja, sem depender exclusivamente de fatores externos (como programas do governo ou investimentos da iniciativa privada de pessoas de fora), a comunidade é a principal responsável pelo seu crescimento. Esse é o fenômeno social da Endogênese.</p>
                  <p>O nome da nossa empresa representa, portanto, nosso desejo em auxiliar as empresas no processo de desenvolvimento interno, por meio de serviços de Consultoria Empresarial e Consultoria em Tecnologia da Informação.</p>
